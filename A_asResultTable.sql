CREATE TABLE "ANALYZER_GOSALES_HR"."A_STORES"(
	store_id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	store_name VARCHAR(150) NOT NULL,
	address_line_1 VARCHAR(255) NOT NULL,
	address_line_2 VARCHAR(100),
	city_id INT NOT NULL,
	state_id INT NOT NULL,
	zip_code VARCHAR(6),
	PRIMARY KEY (store_id)
);




CREATE TABLE "ANALYZER_GOSALES_HR"."A_STORE_DATA" (S_ID,S_NAME) AS
   (SELECT STORE_ID,STORE_NAME
    FROM "ANALYZER_GOSALES_HR"."A_STORES"
   ) WITH DATA;
   




 CREATE TABLE "ANALYZER_GOSALES_HR"."A_STORE_LOCATION" (ADDRESS1,ADDRESS2,CITY,STATE,CODE) AS
   (SELECT address_line_1,address_line_2,city_id,state_id,zip_code
    FROM "ANALYZER_GOSALES_HR"."A_STORES"
   ) WITH DATA EXCLUDING DEFAULTS;